"""
NLU parser for natural-language trading strategy descriptions.

Parses free-form strategy descriptions (Chinese or English) into structured
``StrategyIntent`` objects using OpenAI function calling.  This module is the
first stage of the strategy generation pipeline:

    user describes strategy → **strategy_nlu.parse_strategy()** → code_generator

Typical usage::

    intent = parse_strategy("當RSI低於30時買入，設2%止損，止盈5%")
    print(intent.strategy_type)       # "mean_reversion"
    print(intent.entry_signals[0])    # SignalCondition(indicator='RSI', ...)
    print(intent.risk_rules[0])       # RiskRule(rule_type='stop_loss', ...)
"""


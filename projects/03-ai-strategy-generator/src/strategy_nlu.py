class TradeAction(str, Enum):
    """Supported trade actions."""

    BUY = "BUY"
    SELL = "SELL"
    HOLD = "HOLD"
    ADD = "ADD"
    REDUCE = "REDUCE"


class SignalCondition(BaseModel):
    """A single indicator-based signal condition.

    Represents conditions like ``'RSI < 30'`` or ``'MA(20) crossover MA(60)'``.

    Attributes:
        indicator: Technical indicator name, e.g. ``'RSI'``, ``'MA'``,
            ``'BOLL.lower'``.
        comparison: Comparison operator, e.g. ``'<'``, ``'>'``, ``'crossover'``.
        value: Numeric threshold value.
        params: Additional parameters such as indicator period,
            e.g. ``{'period': 14}``.
    """

    indicator: str = Field(
        ...,
        description=(
            "技術指標名稱，例如 'RSI', 'MA', 'EMA', 'MACD', 'BOLL.upper', "
            "'BOLL.lower', 'VOLUME', 'close', 'KD.K'"
        ),
    )
    comparison: str = Field(
        ...,
        description=(
            "比較運算子: '<', '>', '<=', '>=', '==', 'crossover'（向上穿越/黃金交叉）, "
            "'crossunder'（向下穿越/死亡交叉）"
        ),
    )
    value: float = Field(
        ...,
        description=(
            "閾值數值。例如 RSI < 30 中的 30，止損 2% 中的 0.02"
        ),
    )
    params: Dict[str, Any] = Field(
        default_factory=dict,
        description=(
            "額外參數，例如指標週期 {period: 14}、MACD 快慢線 {fast: 12, slow: 26, signal: 9}"
        ),
    )

    @field_validator("indicator")
    @classmethod
    def validate_indicator(cls, v: str) -> str:
        if not v or not v.strip():
            raise ValueError("indicator must be a non-empty string")
        return v.strip()

    @field_validator("comparison")
    @classmethod
    def validate_comparison(cls, v: str) -> str:
        allowed = {"<", ">", "<=", ">=", "==", "crossover", "crossunder"}
        v = v.strip()
        if v not in allowed:
            raise ValueError(f"comparison must be one of {allowed}, got '{v}'")
        return v


class RiskRule(BaseModel):
    """A risk management rule.

    Attributes:
        rule_type: One of ``'stop_loss'``, ``'take_profit'``,
            ``'trailing_stop'``, ``'position_size'``.
        value: Numeric value for the rule (e.g. 0.02 for 2% stop loss).
        params: Additional parameters, e.g. ``{'method': 'percent'}``
            or ``{'reference': 'entry_price'}``.
    """

    rule_type: str = Field(
        ...,
        description=(
            "風險規則類型: stop_loss（止損）, take_profit（止盈）, "
            "trailing_stop（移動止損）, position_size（倉位大小）"
        ),
    )
    value: float = Field(
        ...,
        description=(
            "規則數值。例如止損 2% → 0.02，止盈 10% → 0.10，"
            "倉位 30% → 0.30"
        ),
    )
    params: Dict[str, Any] = Field(
        default_factory=dict,
        description=(
            "額外參數。例如 {method: 'percent', reference: 'entry_price'}"
        ),
    )

    @field_validator("rule_type")
    @classmethod
    def validate_rule_type(cls, v: str) -> str:
        allowed = {"stop_loss", "take_profit", "trailing_stop", "position_size"}
        v = v.strip()
        if v not in allowed:
            raise ValueError(f"rule_type must be one of {allowed}, got '{v}'")
        return v


class StrategyIntent(BaseModel):
    """Structured strategy intent parsed from natural language.

    This is the primary output of the NLU stage.  It feeds directly into the
    code generator to produce executable trading strategy code.

    Attributes:
        name: Human-readable strategy name generated by the LLM.
        strategy_type: High-level strategy category.
        entry_signals: Conditions that trigger buying or adding positions.
        exit_signals: Conditions that trigger selling or reducing positions.
        risk_rules: Risk management rules (stop loss, take profit, etc.).
        position_sizing: Position sizing method description.
        time_frame: Trading time frame, e.g. ``'daily'``, ``'5min'``.
        description: Brief natural-language summary of the strategy.
    """

    name: str = Field(
        ...,
        description=(
            "策略名稱，簡短描述性名稱，例如 'RSI均值回歸策略'、'MACD趨勢跟蹤'"
        ),
    )
    strategy_type: str = Field(
        ...,
        description=(
            "策略類型: momentum（動量）, mean_reversion（均值回歸）, "
            "breakout（突破）, trend_following（趨勢跟蹤）, multi_factor（多因子）"
        ),
    )
    entry_signals: List[SignalCondition] = Field(
        default_factory=list,
        description="進場信號條件列表（買入/加倉觸發條件）",
    )
    exit_signals: List[SignalCondition] = Field(
        default_factory=list,
        description="出場信號條件列表（賣出/減倉觸發條件）",
    )
    risk_rules: List[RiskRule] = Field(
        default_factory=list,
        description="風險管理規則列表（止損、止盈、移動止損、倉位大小）",
    )
    position_sizing: str = Field(
        default="percent",
        description=(
            "倉位管理方式描述，例如 'fixed', 'percent', 'kelly', "
            "'固定100股', '資金30%'"
        ),
    )
    time_frame: str = Field(
        default="daily",
        description=(
            "交易時間框架: 'daily'（日線）, 'weekly'（週線）, 'monthly'（月線）, "
            "'5min', '15min', '60min' 等"
        ),
    )
    description: str = Field(
        default="",
        description="策略的自然語言摘要描述",
    )

    @field_validator("strategy_type")
    @classmethod
    def validate_strategy_type(cls, v: str) -> str:
        allowed = {
            "momentum",
            "mean_reversion",
            "breakout",
            "trend_following",
            "multi_factor",
        }
        v = v.strip()
        if v not in allowed:
            raise ValueError(f"strategy_type must be one of {allowed}, got '{v}'")
        return v

    @field_validator("name")
    @classmethod
    def validate_name(cls, v: str) -> str:
        if not v or not v.strip():
            raise ValueError("name must be a non-empty string")
        return v.strip()



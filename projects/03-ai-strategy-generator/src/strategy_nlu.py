def parse_strategy(
    user_input: str,
    model: str = "gpt-4o-mini",
) -> StrategyIntent:
    """Parse a natural-language strategy description into structured intent.

    Uses OpenAI function calling to extract a structured ``StrategyIntent``
    from free-form text.  Supports both Chinese (traditional/simplified) and
    English inputs.

    Args:
        user_input: Free-form text describing a trading strategy, e.g.
            ``"當RSI低於30時買入，設2%止損，止盈5%"``.
        model: OpenAI model to use (default ``"gpt-4o-mini"``).

    Returns:
        A validated :class:`StrategyIntent` instance.

    Raises:
        ValueError: If the input is empty or the LLM response cannot be
            parsed or validated.
        RuntimeError: If the OpenAI API call fails.

    Examples:
        >>> intent = parse_strategy("RSI低於30買入，高於70賣出，2%止損")
        >>> intent.strategy_type
        'mean_reversion'
        >>> intent.risk_rules[0].rule_type
        'stop_loss'
    """
    # 輸入驗證
    if not user_input or not user_input.strip():
        raise ValueError("user_input must be a non-empty string")

    client = OpenAI()  # 透過環境變數 OPENAI_API_KEY 讀取金鑰

    # 呼叫 OpenAI function calling
    try:
        response = client.chat.completions.create(
            model=model,
            messages=[
                {"role": "system", "content": _SYSTEM_PROMPT},
                {"role": "user", "content": user_input.strip()},
            ],
            tools=[{"type": "function", "function": _FUNCTION_SCHEMA}],
            tool_choice={
                "type": "function",
                "function": {"name": "parse_strategy_intent"},
            },
            temperature=0.0,  # 確保穩定輸出
        )
    except Exception as exc:
        logger.error("OpenAI API 呼叫失敗: %s", exc)
        raise RuntimeError(f"OpenAI API call failed: {exc}") from exc

    # 解析 function call 回應
    choice = response.choices[0]
    message = choice.message

    # 確認 LLM 有回傳 tool_calls
    if not message.tool_calls:
        raise ValueError(
            "LLM did not return a tool call. "
            f"Finish reason: {choice.finish_reason}. "
            f"Content: {message.content!r}"
        )

    tool_call = message.tool_calls[0]
    try:
        raw_args: Dict[str, Any] = json.loads(tool_call.function.arguments)
    except json.JSONDecodeError as exc:
        raise ValueError(
            f"Failed to parse LLM function arguments as JSON: {exc}"
        ) from exc

    logger.debug(
        "LLM 策略解析結果: %s", json.dumps(raw_args, ensure_ascii=False)
    )

    # 使用 Pydantic 驗證並建構 StrategyIntent
    try:
        intent = StrategyIntent.model_validate(raw_args)
    except Exception as exc:
        raise ValueError(
            f"LLM output failed Pydantic validation: {exc}"
        ) from exc

    return intent


